// Opens a self-contained popup window with the QR code + ticket number,
// triggers print(), and closes itself after printing. Triggered from a user
// click so popup blockers should let it through.
export function printQr(svg, ticketLabel, urlLabel) {
    const win = window.open('', '_blank', 'width=520,height=620,menubar=no,toolbar=no,status=no');
    if (!win) {
        // Popup blocked — fall back to inline print (whole document).
        // The caller can decide whether to retry; we surface the failure
        // so it can show a toast instead.
        return false;
    }

    const escape = (s) => String(s ?? '').replace(/[&<>"']/g, (c) => ({
        '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;'
    })[c]);

    // The SVG is generated by QRCoder server-side; it's our own markup so we
    // pass it through as-is, but escape the ticket-label/url since those are
    // worst-case user-controlled.
    const html = [
        '<!doctype html>',
        '<html><head><meta charset="utf-8"><title>',
        escape(ticketLabel),
        '</title><style>',
        'body { margin: 0; padding: 32px 16px; font-family: Roboto, Arial, sans-serif; text-align: center; }',
        '.qr { display: inline-block; padding: 16px; background: white; }',
        '.qr svg { width: 320px; height: 320px; display: block; }',
        '.label { font-size: 28pt; font-weight: 700; margin: 16px 0 4px; }',
        '.url { font-size: 9pt; color: #666; word-break: break-all; }',
        '@media print { @page { margin: 8mm; size: auto; } body { padding: 0; } }',
        '</style></head><body>',
        '<div class="qr">', svg, '</div>',
        '<div class="label">', escape(ticketLabel), '</div>',
        '<div class="url">', escape(urlLabel), '</div>',
        '<script>',
        'window.addEventListener("load", function () {',
        '  setTimeout(function () { window.print(); }, 100);',
        '});',
        'window.addEventListener("afterprint", function () { window.close(); });',
        '<\/script>',
        '</body></html>'
    ].join('');

    win.document.open();
    win.document.write(html);
    win.document.close();
    return true;
}
